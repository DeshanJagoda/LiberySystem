    void Day(){
        LocalDate date=LocalDate.now();
        int d=date.getDayOfMonth();
        int m=date.getMonthValue();
        int y=date.getYear();
        
        String D=Integer.toString(d);
        String M=Integer.toString(m);
        String Y=Integer.toString(y);
                
        txtToday.setText(D);
        txtToday.setText( txtToday.getText()+"-"+M);
        txtToday.setText( txtToday.getText()+"-"+Y);
        
        txtToday1.setText(D);
        txtToday1.setText( txtToday1.getText()+"-"+M);
        txtToday1.setText( txtToday1.getText()+"-"+Y);
        
        int today=d+14;
        if(today>31){
            int day,month;
            day=today-31;
            month=m+1;
                    
          String day1=Integer.toString(day);
          String Month1 =Integer.toString(month);   
          
         txtLastDay.setText(day1);
         txtLastDay.setText( txtLastDay.getText()+"-"+Month1);
         txtLastDay.setText( txtLastDay.getText()+"-"+Y);
         
        }else{
           String today1=Integer.toString(today);
         txtLastDay.setText(today1);
         txtLastDay.setText( txtLastDay.getText()+"-"+M);
         txtLastDay.setText( txtLastDay.getText()+"-"+Y);
        }
       
    }
    
